#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		c;
	char	*s2;

	c = 0;
	while (s1[c] != 0)
		++c;
	s2 = malloc(sizeof(char) * (c + 1));
	if (s2 == 0)
		return (0);
	c = 0;
	while (s1[c] != 0)
	{
		s2[c] = s1[c];
		++c;
	}
	s2[c] = 0;
	return (s2);
}
